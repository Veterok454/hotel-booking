import transporter from '../configs/nodemailer.js';
import Booking from '../models/Booking.js';
import Hotel from '../models/Hotel.js';
import Room from '../models/Room.js';
import connectDB from '../configs/db.js';
import stripe from 'stripe';

// Function to check room availability
const checkAvailability = async (room, checkInDate, checkOutDate) => {
  try {
    await connectDB();

    const bookings = await Booking.find({
      room,
      checkInDate: { $lte: checkOutDate },
      checkOutDate: { $gte: checkInDate },
    });
    const isAvailable = bookings.length === 0;
    return isAvailable;
  } catch (error) {
    console.error(error.message);
    return false;
  }
};

// API to check room availability
// POST /api/bookings/check-availability
export const checkAvailabilityAPI = async (req, res) => {
  try {
    await connectDB();

    const { room, checkInDate, checkOutDate } = req.body;
    const isAvailable = await checkAvailability(
      room,
      checkInDate,
      checkOutDate
    );
    res.json({ success: true, isAvailable });
  } catch (error) {
    res.json({ success: false, message: error.message });
  }
};

// API to create a new booking
// POST /api/bookings/book
export const createBooking = async (req, res) => {
  try {
    await connectDB();

    const { room, checkInDate, checkOutDate, guests } = req.body;
    const user = req.user._id;

    // Before Booking Check Availability
    const isAvailable = await checkAvailability(
      room,
      checkInDate,
      checkOutDate
    );

    if (!isAvailable) {
      return res.json({
        success: false,
        message: 'Room is not available for the selected dates',
      });
    }

    // Get Total Price from Room
    const roomData = await Room.findById(room).populate('hotel');
    let totalPrice = roomData.pricePerNight;

    // Calculate total price based on number of nights
    const checkIn = new Date(checkInDate);
    const checkOut = new Date(checkOutDate);
    const timeDiff = checkOut.getTime() - checkIn.getTime();
    const nights = Math.ceil(timeDiff / (1000 * 3600 * 24));

    totalPrice *= nights;

    // Create Booking
    const booking = await Booking.create({
      user,
      room,
      hotel: roomData.hotel._id,
      guests: +guests,
      checkInDate,
      checkOutDate,
      totalPrice,
    });

    const hotelName = roomData.hotel.name;
    const hotelAddress = roomData.hotel.address;
    const roomType = roomData.roomType;

    const mailOptions = {
      from: process.env.SENDER_EMAIL,
      to: req.user.email,
      subject: 'Hotel Booking Details',
      html: `
      <h2>Your Booking Details</h2>
      <p>Dear ${req.user.username},</p>
      <p>Thank you for your booking! Here are your details:</p>
      <ul>
      <li><strong>Booking ID:</strong> ${booking._id}</li>
      <li><strong>Hotel Name:</strong> ${hotelName}</li>
       <li><strong>Room Type:</strong> ${roomType}</li>
      <li><strong>Location:</strong> ${hotelAddress}</li>
       <li><strong>Check-in Date:</strong> ${new Date(
         checkInDate
       ).toDateString()}</li>
        <li><strong>Check-out Date:</strong> ${new Date(
          checkOutDate
        ).toDateString()}</li>
        <li><strong>Number of Guests:</strong> ${guests}</li>
        <li><strong>Number of Nights:</strong> ${nights}</li>
      <li><strong>Booking Amount:</strong> ${
        process.env.CURRENCY || 'Â£'
      } ${totalPrice}</li>
            </ul>
            <p>We look forward to welcoming you!</p>
            <p>If you need to make any changes, feel free to contact us.</p>
      `,
    };

    await transporter.sendMail(mailOptions);

    res.json({ success: true, message: 'Booking created successfully' });
  } catch (error) {
    console.log(error);
    res.json({ success: false, message: 'Failed to create booking' });
  }
};

// API to get all bookings for a user
// GET /api/bookings/user
export const getUserBookings = async (req, res) => {
  try {
    await connectDB();

    const user = req.user._id;
    const bookings = await Booking.find({ user })
      .populate('room hotel')
      .sort({ createdAt: -1 });

    res.json({ success: true, bookings });
  } catch (error) {
    res.json({ success: false, message: 'Failed to fetch bookings' });
  }
};

export const getHotelBookings = async (req, res) => {
  try {
    await connectDB();

    const hotel = await Hotel.findOne({ owner: req.auth().userId });

    if (!hotel) {
      return res.json({
        success: false,
        message: 'No hotel found',
      });
    }
    const bookings = await Booking.find({ hotel: hotel._id })
      .populate('room hotel user')
      .sort({ createdAt: -1 });

    // Total Bookings
    const totalBookings = bookings.length;
    // Total Revenue
    const totalRevenue = bookings.reduce(
      (acc, booking) => acc + booking.totalPrice,
      0
    );

    res.json({
      success: true,
      dashboardData: { totalBookings, totalRevenue, bookings },
    });
  } catch (error) {
    res.json({ success: false, message: 'Failed to fetch bookings' });
  }
};

export const stripePayment = async (req, res) => {
  try {
    await connectDB();

    const { bookingId } = req.body;
    const booking = await Booking.findById(bookingId);
    const roomData = await Room.findById(booking.room).populate('hotel');
    const totalPrice = booking.totalPrice;
    const { origin } = req.headers;

    const stripeInstance = new stripe(process.env.STRIPE_SECRET_KEY);

    const line_items = [
      {
        price_data: {
          currency: 'gbp',
          product_data: {
            name: roomData.hotel.name,
          },
          unit_amount: totalPrice * 100,
        },
        quantity: 1,
      },
    ];

    //Create Checkout Session
    const session = await stripeInstance.checkout.sessions.create({
      line_items,
      mode: 'payment',
      success_url: `${origin}/loader/my-bookings`,
      cancel_url: `${origin}/my-bookings`,
      metadata: {
        bookingId,
      },
    });
    res.json({
      success: true,
      url: session.url,
    });
  } catch (error) {
    res.json({ success: false, message: 'Payment failed' });
  }
};
